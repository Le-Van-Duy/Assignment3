    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String taiKhoan = txtUser.getText().trim();
        String matKhau = String.valueOf(txtPass.getPassword()).trim();
        String role = userService.checkLogin(taiKhoan, matKhau);
        if (taiKhoan.isBlank() || taiKhoan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pleas fill in UserName box !", "ERORR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (matKhau.isBlank() || matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pleas fill in PassWord box !", "ERORR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (role != null) {
            JOptionPane.showMessageDialog(this, "Login successful !");
            if (role.equals("Student")) {
                new ViewStudent().setVisible(true);
            } else if (role.equals("Grade")) {
                new ViewGrade().setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Login fail !", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnLoginActionPerformed

