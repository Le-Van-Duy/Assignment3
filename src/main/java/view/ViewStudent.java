    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Student student = new Student();
        student.setMaSV(txtMaSV.getText());
        student.setHoTen(txtHoTen.getText());
        student.setSdt(txtSdt.getText());
        student.setEmail(txtEmail.getText());
        student.setDiaChi(txtDiachi.getText());
        student.setGioiTinh(rdoNam.isSelected());
        student.setHinh(lblImage.getText());
        String kq = studentServcice.add(student);
        JOptionPane.showMessageDialog(this, kq);
        showDataTable(studentServcice.getAll());
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = tblStudent.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Please select the row you want to Delete !", "ERORR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String maSV = txtMaSV.getText();
        String kq = studentServcice.delete(maSV);
        JOptionPane.showMessageDialog(this, kq);
        showDataTable(studentServcice.getAll());
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tblStudent.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Please select the row you want to Update !", "ERORR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Student student = new Student();
        student.setMaSV(txtMaSV.getText());
        student.setHoTen(txtHoTen.getText());
        student.setSdt(txtSdt.getText());
        student.setEmail(txtEmail.getText());
        student.setDiaChi(txtDiachi.getText());
        student.setGioiTinh(rdoNam.isSelected());
        student.setHinh(lblImage.getText());
        String kq = studentServcice.update(student);
        JOptionPane.showMessageDialog(this, kq);
        showDataTable(studentServcice.getAll());


    }//GEN-LAST:event_btnUpdateActionPerformed

