    private void loadTable() {
        String[] columns = {"Ma SV", "Name", "Email", "Phone", "Gender", "Adress", "IMG"};
        dtm.setColumnIdentifiers(columns);
        tblStudent.setModel(dtm);
        showDataTable(studentServcice.getAll());
    }

    private void showDataTable(List<Student> lists) {
        dtm.setRowCount(0);
        for (Student sv : lists) {
            dtm.addRow(sv.toDataRow());
        }
    }

    private void fillPhanTu(String maSV) {
        Student student = studentServcice.findByMaSV(maSV);
        txtMaSV.setText(student.getMaSV());
        txtHoTen.setText(student.getHoTen());
        txtSdt.setText(student.getSdt());
        txtEmail.setText(student.getEmail());
        txtDiachi.setText(student.getDiaChi());
        lblImage.setText(student.getHinh());
        if (student.isGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

